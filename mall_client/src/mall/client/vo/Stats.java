package mall.client.vo;

public class Stats {
	private String statsDay;
	private long statsCount;
	
	public String getStatsDay() {
		return statsDay;
	}
	public void setStatsDay(String statsDay) {
		this.statsDay = statsDay;
	}
	public long getStatsCount() {
		return statsCount;
	}
	public void setStatsCount(long statsCount) {
		this.statsCount = statsCount;
	}
	@Override
	public String toString() {
		return "Stats [statsDay=" + statsDay + ", statsCount=" + statsCount + "]";
	}
}
